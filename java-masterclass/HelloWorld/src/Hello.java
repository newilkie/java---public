public class Hello {

    public static void main(String[] args) {
        System.out.println("Hello World"); // you have defined your first string to be printed.
        System.out.println("Hello, Nathan"); // You've added your name as a printed line.
        // Note the quotes " are the indicator that you have started and ended a string literal"

        int myFirstNumber = (10 + 5) + (2 * 10); //you have defined your first variable.
        int mySecondNumber = 12;
        int myThirdNumber = myFirstNumber * 2;
        int myTotal = myFirstNumber + mySecondNumber + myThirdNumber;
        System.out.println(myTotal);
        int myLastOne = 1000 - myTotal;
        System.out.println(myLastOne); //use sout to get it to start "sout" is a shortcut for System.out.println()
    }
}
