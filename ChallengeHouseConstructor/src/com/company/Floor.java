package com.company;

public class Floor {
    private String floorType;
    private String floorColor;

    public Floor(String floorType, String floorColor) {
        this.floorType = floorType;
        this.floorColor = floorColor;
    }

    public String getFloorType() {
        return floorType;
    }

    public String getFloorColor() {
        return floorColor;
    }
}
